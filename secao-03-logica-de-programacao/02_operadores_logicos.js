/* 
Operadores Lógicos
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO
*/



console.log(4 > 3 && 3 > 3) //false
console.log(4 > 3 && 5 > 3) //Os dois precisam ser verdadeiros para retornar true
console.log('Cat' && 'Dog') //returns Dog returns the last true statement
console.log(5 == 4 || 7 != 6)// Apenas um precisa ser verdade para retornar true
console.log('Cat' || 'Dog') //returns Cat returns first true statement
console.log(!false) //Nega o false, inverte o valor
console.log(!!true) // Nega o true duas vezes

