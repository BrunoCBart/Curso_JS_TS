/** 
 * Aritméticos
 * + Adição / Concatenação
 * ** Potenciação
 * % Resto da divisão
 * / divisão
 * * multiplicação
*/

/*
Incremento = ++
Decremento = --
*/

let num1 = 5
let num2 = 10
let num3 = '5'

num1++
num2--

num3 += '10'

console.log(num1 * num2)
console.log(num1 ** num2)
console.log(num1 / num2)
console.log(num1 + num2)
console.log(num2 + num3) //concatenação 